/**
 * Поиск рецепта по названию
 * @param {string} query - поисковый запрос
 */
function findRecipe(query) {
    const normalizedQuery = query.toLowerCase().trim();
    
    // Ищем точное совпадение или частичное
    let recipeKey = null;
    
    // Сначала ищем точное совпадение
    if (recipes[normalizedQuery]) {
        recipeKey = normalizedQuery;
    } else {
        // Ищем частичное совпадение
        recipeKey = Object.keys(recipes).find(key => 
            key.includes(normalizedQuery) || 
            recipes[key].title.toLowerCase().includes(normalizedQuery)
        );
    }
    
    if (recipeKey) {
        displayRecipe(recipeKey);
    } else {
        showNoRecipeMessage();
    }
}

