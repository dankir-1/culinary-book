/**
 * Перевод текста на русский язык
 * @param {string} text - текст для перевода
 * @returns {string} - переведенный текст
 */
function translateToRussian(text) {
    if (!text) return text;
    
    // Ищем точное совпадение
    if (translationDictionary[text]) {
        return translationDictionary[text];
    }
    
    // Ищем частичное совпадение (более точное)
    const lowerText = text.toLowerCase();
    for (const [english, russian] of Object.entries(translationDictionary)) {
        const lowerEnglish = english.toLowerCase();
        
        // Точное совпадение без учета регистра
        if (lowerText === lowerEnglish) {
            return russian;
        }
        
        // Проверяем содержится ли английское название в тексте
        if (lowerText.includes(lowerEnglish) && lowerEnglish.length > 3) {
            return russian;
        }
        
        // Проверяем содержится ли текст в английском названии
        if (lowerEnglish.includes(lowerText) && lowerText.length > 3) {
            return russian;
        }
    }
    
    // Дополнительная обработка для составных слов
    const words = text.split(' ');
    if (words.length > 1) {
        const translatedWords = words.map(word => {
            const lowerWord = word.toLowerCase();
            for (const [english, russian] of Object.entries(translationDictionary)) {
                if (lowerWord === english.toLowerCase() || 
                    lowerWord.includes(english.toLowerCase()) ||
                    english.toLowerCase().includes(lowerWord)) {
                    return russian;
                }
            }
            return word;
        });
        
        // Если хотя бы одно слово было переведено, возвращаем результат
        if (translatedWords.some((word, index) => word !== words[index])) {
            return translatedWords.join(' ');
        }
    }
    
    return text;
}

