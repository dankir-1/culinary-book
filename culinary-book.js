/**
 * Поиск рецептов по частичному совпадению
 * @param {string} query - поисковый запрос
 * @returns {Array} - массив подходящих рецептов
 */
function searchRecipesByPartialMatch(query) {
    const normalizedQuery = query.toLowerCase().trim();
    if (normalizedQuery.length < 2) return [];
    
    const allTitles = getAllRecipeTitles();
    const matches = [];
    
    allTitles.forEach(title => {
        const normalizedTitle = title.toLowerCase();
        
        // Ищем совпадения в начале слов
        if (normalizedTitle.includes(normalizedQuery)) {
            matches.push(title);
        }
    });
    
    // Сортируем по релевантности (точные совпадения в начале)
    matches.sort((a, b) => {
        const aLower = a.toLowerCase();
        const bLower = b.toLowerCase();
        
        // Точные совпадения в начале
        if (aLower.startsWith(normalizedQuery) && !bLower.startsWith(normalizedQuery)) return -1;
        if (!aLower.startsWith(normalizedQuery) && bLower.startsWith(normalizedQuery)) return 1;
        
        // Алфавитный порядок
        return a.localeCompare(b);
    });
    
    return matches;
}

