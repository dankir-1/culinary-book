/**
 * Сохранение нового рецепта
 */
function saveNewRecipe(event) {
    event.preventDefault();
    
    const name = recipeName.value.toLowerCase().trim();
    const title = recipeName.value.trim();
    const ingredientsText = recipeIngredients.value.trim();
    const stepsText = recipeSteps.value.trim();
    
    // Разбиваем текст на массивы по строкам
    const ingredients = ingredientsText.split('\n')
        .map(line => line.trim())
        .filter(line => line.length > 0);
    
    const instructions = stepsText.split('\n')
        .map(line => line.trim())
        .filter(line => line.length > 0);
    
    // Проверяем, что все поля заполнены
    if (!name || ingredients.length === 0 || instructions.length === 0) {
        alert('Пожалуйста, заполните все поля формы');
        return;
    }
    
    // Добавляем рецепт в базу данных
    recipes[name] = {
        title: title,
        ingredients: ingredients,
        instructions: instructions,
        time: "Не указано",
        servings: "Не указано"
    };
    
    // Сохраняем в localStorage
    saveRecipesToStorage();
    
    // Закрываем модальное окно
    closeAddRecipeModal();
    
    // Показываем добавленный рецепт
    displayRecipe(name);
    
    // Обновляем поле поиска
    searchInput.value = name;
    
    // Обновляем отображение всех рецептов
    displayAllRussianRecipes();
    
    // Показываем уведомление об успешном добавлении
    showSuccessNotification('Рецепт успешно добавлен и сохранен!');
}

